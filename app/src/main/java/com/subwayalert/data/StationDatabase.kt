    fun getLines(city: String): List<String> = cities[city] ?: emptyList()

    fun getStations(city: String, line: String): List<String> {
        return stations[city]?.get(line) ?: emptyList()
    }

    fun findStation(city: String, line: String, name: String): String? {
        val normalized = name.replace("站", "").trim()
        val stationList = getStations(city, line)
        
        // 精确匹配
        stationList.forEach { s ->
            if (s == normalized || s.replace("站", "") == normalized) {
                return s
            }
        }
        
        // 包含匹配
        stationList.forEach { s ->
            if (normalized in s || s in normalized) {
                return s
            }
        }
        
        return null
    }

    fun getStationIndex(city: String, line: String, station: String): Int {
        return getStations(city, line).indexOf(station)
    }

    fun calculateRemainingStations(city: String, line: String, currentStation: String, endStation: String): Int {
        val currentIdx = getStationIndex(city, line, currentStation)
        val endIdx = getStationIndex(city, line, endStation)
        
        if (currentIdx < 0 || endIdx < 0) return -1
        return endIdx - currentIdx
    }
